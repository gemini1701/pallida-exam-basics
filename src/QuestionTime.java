    /*Function

    A function is a piece of code that is called by name.
    It can be passed data to operate on and can optionally return data (the return value).
    All data that is passed to a function is explicitly passed.


    Method

    A method is a piece of code that is called by a name that is associated with an object.
    In most respects it is identical to a function except for two key differences.

        A method is implicitly passed the object on which it was called.
        A method is able to operate on data that is contained within the class.
   */



    /* Constructor

    A constructor in Java is a block of code similar to a method that’s called when an instance of an object is created.
    Here are the key differences between a constructor and a method.

    A constructor doesn’t have a return type
    The name of the constructor must be the same as the name of the class
    Unlike methods, constructors are not considered members of a class
    A constructor is called automatically when a new instance of an object is created
    */

